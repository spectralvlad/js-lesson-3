// Алгебраїчна сума '2' + '4' -> 6
console.log(parseInt("2") + parseInt("4")); // Результат: 6

// Ділення двох чисел 7.2 / '1.2' -> 6
console.log(parseFloat(7.2) / parseFloat("1.2")); // Результат: 6

// Конкатенація строк 'this' + ' simple' + ' string' -> 'this simple string'
console.log("this" + " simple" + " string"); // Результат: 'this simple string'

// Сума з конкатенацією 'answer' + ' is:' + '30' + 5 -> 'answer is 35'
console.log("answer" + " is: " + (parseInt("30") + 5)); // Результат: 'answer is 35'
