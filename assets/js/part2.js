// Алгебраїчна сума
let result1 = Number("2") + Number("4");
console.log(result1); // Виведе: 6

// Ділення двох чисел
let result2 = 7.2 / parseFloat("1.2");
console.log(result2); // Виведе: 6

// Конкатенація строк
let result3 = "this" + " simple" + " string";
console.log(result3); // Виведе: 'this simple string'

// Сума з конкатенацією
let result4 = "answer" + " is: " + (Number("30") + Number(5));
console.log(result4); // Виведе: 'answer is: 35'
